class CommandeAPITests(APITestCase):
    def setUp(self):
        """Crée un fournisseur pour les tests de commandes."""
        self.fournisseur = Fournisseur.objects.create(
            name="Grand Fournisseur",
            address="456 Avenue des Commandes",
            phone="0987654321",
            email="commandes@fournisseur.com"
        )
        self.url = reverse('commande-list')

    def test_create_commande(self):
        """Vérifie qu'on peut créer une commande sans fournir de total."""
        data = {'fournisseur': self.fournisseur.id}
        response = self.client.post(self.url, data, format='json')

        self.assertEqual(response.status_code, status.HTTP_201_CREATED)
        self.assertEqual(Commande.objects.count(), 1)
        # Vérifie que la réponse contient le total calculé (qui est 0.00 pour une nouvelle commande)
        self.assertEqual(response.data['total'], "0.00")

        commande = Commande.objects.get()
        self.assertEqual(commande.fournisseur.id, self.fournisseur.id)



