class RayonAPITests(APITestCase):
    def test_create_rayon(self):
        """Ensure we can create a new rayon object."""
        url = reverse('rayon-list') # 'rayon-list' is generated by the router
        data = {'name': 'Nouveau Rayon'}
        response = self.client.post(url, data, format='json')
        self.assertEqual(response.status_code, status.HTTP_201_CREATED)
        self.assertEqual(Rayon.objects.count(), 1)
        self.assertEqual(Rayon.objects.get().name, 'Nouveau Rayon')

